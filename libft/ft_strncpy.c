#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	size_t length;
	size_t i;

	length = 0;
	i = 0;
	while (src[length++])
		;
	while (i < length && i < len)
	{
		dst[i] = src[i];
		i++;
	}
	while (i < len)
	{
		dst[i] = 0;
		i++;
	}
	return (dst);
}
